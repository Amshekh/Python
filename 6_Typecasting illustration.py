# To typecast int to float, float to int etc
x = float(2)
print(x)   # Here value of int type 2 is typecast to float type and hence output will be 2.0

y = int(3.14)
print(y)    # Here 3.14 is float type and we are going to typecast it into int type, output will be 3

# Now we are going to typecast a integer to string
a = str(5)
b = 4
c = a + b
print(c)  # There will be error as a which is now a string can not be operated with b which is int type