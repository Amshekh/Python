# Operators in Python
a = 5
b = 7
c = a == b  #  == operator checks equality and assigns true or false value accordingly. In this case false
print(c)

x = a != b # != will check if a is not equal to b which is true in this case, so output will be true
print(x)

y = a > b  #  here a is not greater than b, so y will be assigned false
print(y)